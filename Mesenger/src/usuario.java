
public class usuario {
	
	private int id;
	private String nombre;
	private String mensajeTX;
	private String mensajeRX;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getMensajeTX() {
		return mensajeTX;
	}
	public void setMensajeTX(String mensajeTX) {
		this.mensajeTX = mensajeTX;
	}
	public String getMensajeRX() {
		return mensajeRX;
	}
	public void setMensajeRX(String mensajeRX) {
		this.mensajeRX = mensajeRX;
	}

	
	
	
	
}
