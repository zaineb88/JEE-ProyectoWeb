package proyectoweb1;

public class Actorazo {

	private String nombre;
	private int aded;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getAded() {
		return aded;
	}
	public void setAded(int aded) {
		this.aded = aded;
	}
	public Actorazo(String nombre, int aded) {
		super();
		this.nombre = nombre;
		this.aded = aded;
	}

	
	
}
